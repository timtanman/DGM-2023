using System;
public class greater_than
{
	public void Main()
	{
	int x = 10;
	int y = 10;
	if (x > y)
		{
  		Console.WriteLine("Hello World");
		} else if (x != y)
	{
		Console.WriteLine("X does not equal Y");
	} else if (x == y)
	{
		Console.WriteLine("X does equal Y");
	} else
	{
		Console.WriteLine("X is less than Y");
	}
}
}